// Calcular area de um retangulo

/*Escreva um algoritmo para ler as dimensões de um retângulo (base e altura), 
calcular e escrever a área do retângulo.*/

let base, altura, resultado;

base=parseFloat(prompt("Digite a base"));
altura=parseFloat(prompt("Digite a altura"));
resultado= base * altura;
console.log("O resultado da area do retangulo e: ",resultado);